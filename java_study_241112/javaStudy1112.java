package java_study_241112;

// 자바 공부를 시작하겠습니다. (메모 >> 주석)
// 자바는 한글 지원이 가능합니다.

public class javaStudy1112 {

	public static void main(String[] args) {
		// 이안에 코드를 작성할 예정
		// 문장 마지막에는 세미콜론을 작성한다.
		System.out.println("안녕하세요, 오늘 자바를 배웁니다.");
		// ctrl + F11을 누르면Java가 실행됨 
		
		System.out.println("Hello World!");
		
		// syso 입력 후 ctrl + space 이후 enter(자동 입력 단축키)
		System.out.println("자동입력 단축키 사용법");
		System.out.println("반갑습니다.");
		System.out.println("아무말");
		System.out.println("대잔치");
		System.out.println("A");
		System.out.println("B");
		// 코드 작성 후 ctrl + s 눌러서 저장 필수(이클립스는 자동저장 안됨)
		
		System.out.println("현재 년도는 : " + 2024 + " 입니다.");
		// 출력문에서 +는 문자와 문자를 합쳐준다는 의미
		
		System.out.println("키: " + 158 + " 입니다.");
		System.out.println("나이: " + 20 + " 입니다.");
		
		// ln은 개행(줄바꿈)을 의미합니다. 
		// ln이 없으면 가로로 쭉 출력됩니다.
		System.out.print("안녕하세요.");
		System.out.println("안녕하세요.");
		
		// 변수: 어떤 값을 대입할 떄 사용
		// int는 숫자를 의미, String은 문자를 의미합니다.
		int age = 20;
		String address = "대전 서구 둔산동";
		String mbti = "ESFJ";
		
		System.out.println("나이는: " + age);
		System.out.println("주소는: " + address);
		System.out.println("MBTI: " + mbti);
		
		

	}

}
